namespace HTML_Araştırma_ödevi;


// Temel Yapı Etiketleri
// 1.<html>: HTML belgesinin başlangıcını ve sonunu belirtir.
// <head>: Sayfanın başlık, stil ve diğer meta bilgilerini içeren bölüm.
// <title>: Tarayıcı sekmesinde görünen sayfa başlığını belirtir.
// <meta>: Sayfa hakkında bilgi veren (örneğin, karakter seti, sayfa açıklaması) meta verileri tanımlar.
// <link>: Dış kaynakları (örneğin, CSS dosyaları) bağlar.
// <style>: Sayfanın stil bilgilerini içerir.
// <script>: JavaScript kodları eklemek için kullanılır.
// <body>: Sayfanın görünür içeriğini (metin, görseller, vb.) tutar.

// 2. Başlık ve Paragraf Etiketleri
// <h1> - <h6>: Başlık etiketleri, hiyerarşide<h1> en yüksek başlıkken, <h6> en düşük başlıktır.SEO açısından önemlidir.
// <p>: Paragraf etiketi, metin bloklarını oluşturur.
// <br>: Satır sonu ekler.
// <hr>: Yatay bir çizgi çizer, içerik arasında ayırıcı görevi görür.

// 3. Metin Biçimlendirme Etiketleri
// <strong>: Vurgulamak için kalın yazı oluşturur (anlamı güçlendirir).
// <b>: Sadece görsel olarak kalın yapar, anlamı güçlendirmez.
// <em>: İtalik metin oluşturur, anlamı vurgular.
// <i>: Sadece görsel olarak italik yapar.
// <u>: Altı çizili metin oluşturur.
// <mark>: Vurgulanmış metin (genellikle sarı arka plan).
// <sup>: Üst simge(örneğin, ²).
// <sub>: Alt simge(örneğin, H₂O).
// <blockquote>: Uzun alıntılar için kullanılır.
// <cite>: Kaynak belirtir.

// 4. Liste Etiketleri
// <ul>: Sırasız liste (madde işaretli).
// <ol>: Sıralı liste (numaralı).
// <li>: Liste öğesi.



// 5. Bağlantı ve Medya Etiketleri
// <a>: Bağlantı oluşturur (href ile hedef belirtilir).
// <img>: Görsel eklemek için kullanılır(src ile kaynak belirtilir).
// <iframe>: Diğer bir sayfayı içine yerleştirir.
// <audio>: Ses dosyası eklemek için kullanılır.
// <video>: Video dosyası eklemek için kullanılır.
// <source>: Medya kaynağını tanımlar(özellikle ses ve video için).
// <picture>: Farklı görsel kaynakları destekler.
// <figcaption>: Görselin açıklamasını sağlar.
// <figure>: Görsel ve açıklamasını gruplar.



// 6. Tablo Etiketleri
// <table>: Tablo oluşturur.
// <thead>, <tbody>, <tfoot>: Tablo başlığı, gövdesi ve alt kısmı.
// <tr>: Tablo satırı.
// <td>: Tablo hücresi.
// <th>: Başlık hücresi.
// <caption>: Tablo başlığı.


// 7. Form ve Girdi Etiketleri
// <form>: Formun başlangıcını belirtir.
// <input>: Kullanıcıdan girdi almak için kullanılır (çeşitli tipleri vardır).
// <textarea>: Çok satırlı metin girişi için kullanılır.
// <button>: Buton oluşturur.
// <label>: Girdi elemanlarına açıklama ekler.
// <select>, <option>, <optgroup>: Seçim kutusu oluşturur.


// 8. Bölümleme Etiketleri
// <div>: Blok düzeyinde element, genel yapı oluşturur.
// <span>: Satır içi element, küçük gruplamalar için kullanılır.
// <section>, <article>, <aside>, <header>, <footer>, <nav>, <main>: HTML5 ile gelen yapısal etiketler.


// 9. Diğer Faydalı Etiketler
// <details>, <summary>: Açılır/kapanır içerik oluşturur.
// <dialog>: Diyalog kutusu oluşturur.


// 10. Yazılım Geliştirme ile İlgili Etiketler
// <script>: JavaScript kodları için.
// <noscript>: JavaScript desteklenmiyorsa gösterilecek içerik.
// <template>: Dinamik içerik oluşturmak için